export function getShoe(id: string): Shoe | undefined {
  return shoes.find((s) => s.id === id)
}

export function shoesByBrand(): Record<string, number> {
  return shoes.reduce<Record<string, number>>((acc, shoe) => {
    acc[shoe.brand] = (acc[shoe.brand] ?? 0) + 1
    return acc
  }, {})
}
