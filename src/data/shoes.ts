export type ArchSupport = 'low' | 'medium' | 'high'
export type Stability = 'neutral' | 'stability' | 'motion-control'
export type Cushion = 'firm' | 'balanced' | 'max'
export type PriceBand = 'budget' | 'mid' | 'premium'
export type Surface = 'road' | 'trail' | 'track' | 'gym'
export type Width = 'standard' | 'wide' | 'both'

export interface Shoe {
  id: string
  name: string
  brand: string
  /** Approximate street / promo price in MYR (check stores before buying) */
  priceMyr: number
  priceBand: PriceBand
  archSupport: ArchSupport
  stability: Stability
  cushion: Cushion
  dropMm: number
  /** Approx men's US9 weight in grams */
  weightG: number
  surfaces: Surface[]
  bestFor: string[]
  width: Width
  color: string
  accent: string
  summary: string
  highlights: string[]
  /** Local product image under /public/shoes */
  image: string
  /**
   * Primary “Research / buy” button.
   * Put your affiliate URL here when approved (Shopee, Lazada, brand program).
   * Until then, a normal product / brand page is fine.
   */
  buyUrl: string
  officialUrl: string
}

/** Shopee Malaysia search for a model (not affiliate by default). */
export function shopeeSearchUrl(shoe: Pick<Shoe, 'brand' | 'name'>): string {
  const q = encodeURIComponent(`${shoe.brand} ${shoe.name} running`)
  return `https://shopee.com.my/search?keyword=${q}`
}

/** Lazada Malaysia search for a model (not affiliate by default). */
export function lazadaSearchUrl(shoe: Pick<Shoe, 'brand' | 'name'>): string {
  const q = encodeURIComponent(`${shoe.brand} ${shoe.name}`)
  return `https://www.lazada.com.my/catalog/?q=${q}`
}

function s(
  partial: Omit<Shoe, 'image'> & { image?: string },
): Shoe {
  return {
    ...partial,
    image: partial.image ?? `/shoes/${partial.id}.jpg`,
  }
}

